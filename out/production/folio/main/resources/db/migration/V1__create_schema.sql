CREATE TABLE currency (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name VARCHAR(3) NOT NULL UNIQUE
);

CREATE TABLE isin (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    isin VARCHAR(12) NOT NULL UNIQUE
);

CREATE TABLE isin_name (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    isin_id INTEGER NOT NULL REFERENCES isin(id),
    name VARCHAR(255) NOT NULL,
    UNIQUE (isin_id, name)
);

CREATE TABLE ticker_symbol (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    symbol VARCHAR(20) NOT NULL UNIQUE
);

CREATE TABLE isin_ticker (
    isin_id INTEGER NOT NULL REFERENCES isin(id),
    ticker_symbol_id INTEGER NOT NULL REFERENCES ticker_symbol(id),
    PRIMARY KEY (isin_id, ticker_symbol_id)
);

CREATE TABLE country (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE branch (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE isin_country (
    isin_id INTEGER NOT NULL REFERENCES isin(id),
    country_id INTEGER NOT NULL REFERENCES country(id),
    PRIMARY KEY (isin_id, country_id)
);

CREATE TABLE isin_branch (
    isin_id INTEGER NOT NULL REFERENCES isin(id),
    branch_id INTEGER NOT NULL REFERENCES branch(id),
    PRIMARY KEY (isin_id, branch_id)
);

CREATE TABLE depot (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE "transaction" (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    "date" TIMESTAMP NOT NULL,
    isin_id INTEGER NOT NULL REFERENCES isin(id),
    depot_id INTEGER NOT NULL REFERENCES depot(id),
    "count" DOUBLE PRECISION NOT NULL,
    share_price DOUBLE PRECISION NOT NULL
);

CREATE TABLE dividend (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    isin_id INTEGER NOT NULL REFERENCES isin(id),
    currency_id INTEGER NOT NULL REFERENCES currency(id),
    dividend_per_share DOUBLE PRECISION NOT NULL
);

CREATE TABLE dividend_payment (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    "timestamp" TIMESTAMP NOT NULL,
    isin_id INTEGER NOT NULL REFERENCES isin(id),
    depot_id INTEGER NOT NULL REFERENCES depot(id),
    currency_id INTEGER NOT NULL REFERENCES currency(id),
    "value" DOUBLE PRECISION NOT NULL
);

CREATE TABLE quote_provider (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE isin_quote (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    isin_id INTEGER NOT NULL UNIQUE REFERENCES isin(id),
    quote_provider_id INTEGER NOT NULL REFERENCES quote_provider(id),
    "value" DOUBLE PRECISION NOT NULL,
    fetched_at TIMESTAMP NOT NULL
);

CREATE TABLE settings (
    id INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    "key" VARCHAR(100) NOT NULL UNIQUE,
    "value" VARCHAR(500) NOT NULL
);
